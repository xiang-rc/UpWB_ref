void generatesbox(u8 P[256][256][2], u8 C[256][256][2], u8 rk[roundaes16 + 1][2])
{
	for (u8 j = 0x00; j <= 0xff; j++)  // 0xff+1=0xfe
	{
		for (u8 i = 0x00; i <= 0xff; i++)
		{
			P[j][i][0] = j;   P[j][i][1] = i;   // initilize plaintext pairs (lexicographical)
			AES16Encrypt(P[j][i], C[j][i], rk);
			//printf("0x%02X%02X--->", P[j][i][0], P[j][i][1]);
			//AES16Decrypt(C[j][i], P[j][i], rk);   //verify encryption is correct
			printf("0x%02X%02X,", C[j][i][0], C[j][i][1]);
			if (i == 0xff)   // be careful about dead loop
				break;
		}
		if (j == 0xff)
			break;
	}
}
void generateinvsbox(u8 PP[256][256][2], u8 CC[256][256][2], u8 rk[roundaes16 + 1][2])
{
	for (u8 j = 0; j <= 0xff; j++)
	{
		for (u8 i = 0; i <= 0xff; i++)
		{
			CC[j][i][0] = j;   CC[j][i][1] = i;   // initilize ciphertext pairs (lexicographical)
			AES16Decrypt(CC[j][i], PP[j][i], rk);
			printf("0x%02X%02X,", PP[j][i][0], PP[j][i][1]);
			if (i == 0xff)
				break;
		}
		if (j == 0xff)
			break;
	}
}

