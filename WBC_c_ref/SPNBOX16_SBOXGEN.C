int modulus[] = { 1, 1, 0, 1, 1, 0, 0, 0, 1 }; // x^8 + x^4 + x^3 + x + 1 (AES) (right is high-order)
GFqDom<int32_t> GF28(2, 8, modulus);

/**************       AES16  ***************************/
void AES16KeySchedule(u8 rk[roundaes16 + 1][2])
{
	u8 i;
	for (i = 0; i < roundaes16 + 1; ++i)
	{
		//rk[i] = rand();
		rk[i][0] = i; //ignore KDF, we fix the key
		rk[i][1] = i + 1;
	}
}

